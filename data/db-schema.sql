	against_bug FLOAT,
	against_dark FLOAT,
	against_dragon FLOAT,
	against_electric FLOAT,
	against_fairy FLOAT,
	against_fight FLOAT,
	against_fire FLOAT,
	against_flying FLOAT,
	against_ghost FLOAT,
	against_grass FLOAT,
	against_ground FLOAT,
	against_ice FLOAT,
	against_normal FLOAT,
	against_poison FLOAT,
	against_psychic FLOAT,
	against_rock FLOAT,
	against_steel FLOAT,
	against_water FLOAT,
	attack INT,
	base_egg_steps INT,
	base_happiness INT,
	base_total INT,
	defense INT,
	experience_growth INT,
	height_m FLOAT,
	hp INT,
	name VARCHAR(50),
	percentage_male FLOAT,
	pokedex_number INT,
	sp_attack INT,
	sp_defense INT,
	speed INT,
	type1 VARCHAR(50),
	type2 VARCHAR(50),
	weight_kg FLOAT,
